package Fundamentals;
import java.util.*;

public class star2 {
	 public static void main(String args[]){
	        int i=1,j,n;
	        
	        Scanner sc=new Scanner(System.in);
	     n=sc.nextInt();
	     while(i<=n){
	        for(j=1;j<=i;j++)
	         System.out.print("* ");
	        i++;
	        System.out.println(" ");
	    }
	     
	        
	        
	    }

}
